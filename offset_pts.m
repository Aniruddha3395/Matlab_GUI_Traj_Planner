function [p3,p4] = offset_pts(p1,p2,d)

p3(1) = p1(1) + d*(p1(2)-p2(2))/abs(sqrt((p2(1)-p1(1))^2 + (p2(2)-p1(2))^2));
p3(2) = p1(2) + d*(p2(1)-p1(1))/abs(sqrt((p2(1)-p1(1))^2 + (p2(2)-p1(2))^2));
p4(1) = p2(1) - d*(p2(2)-p1(2))/abs(sqrt((p2(1)-p1(1))^2 + (p2(2)-p1(2))^2));
p4(2) = p2(2) - d*(p1(1)-p2(1))/abs(sqrt((p2(1)-p1(1))^2 + (p2(2)-p1(2))^2));

end